    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            emp = new Employee();
            emp.setName(tfName.getText().trim());
            emp.setAge(Integer.parseInt(tfAge.getText().trim()));
            emp.setAddress(tfAddress.getText().trim());
            emp.setIncome(Integer.parseInt(tfIncome.getText().trim()));
            
            if (tfName.getText().trim().equals("") || tfAddress.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(this, "Please fill out all fields with appropriate data");
                return;
            }
            
            this.setVisible(false);
            Repo.insert(emp);
            Main.repopulate();
        }  catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Please fill out all fields with appropriate data");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

